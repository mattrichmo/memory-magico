const COMMAND_BY_NAME = new Map();
for (const command of COMMANDS) {
  if (COMMAND_BY_NAME.has(command.name)) {
    throw new Error(`Duplicate command registration: ${command.name}`);
  }
  COMMAND_BY_NAME.set(command.name, command);
  for (const alias of command.aliases || []) {
    if (COMMAND_BY_NAME.has(alias)) {
      throw new Error(`Duplicate command alias registration: ${alias}`);
    }
    COMMAND_BY_NAME.set(alias, command);
  }
}

export function listCommands() {
  return COMMANDS.map(command => ({ ...command }));
}

export function getCommand(name) {
  return COMMAND_BY_NAME.get(name) || null;
}

export function getCommandByName(name) {
  return COMMANDS.find(command => command.name === name) || null;
}

export function groupCommands() {
  const groups = new Map();
  for (const command of COMMANDS) {
    const group = groups.get(command.category) || [];
    group.push(command);
    groups.set(command.category, group);
  }
  return groups;
}
